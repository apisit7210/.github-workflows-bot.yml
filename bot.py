def log_signal(symbol, signal, conf, price, SL, TP):
    log = load_trade_log()
    log.append({
        'ts':      datetime.utcnow().isoformat(),
        'symbol':  symbol,
        'signal':  signal,
        'conf':    round(conf, 4),
        'price':   round(price, 6),
        'SL':      round(SL, 6),
        'TP':      round(TP, 6),
        'outcome': None,   # จะ update ทีหลัง
    })
    save_trade_log(log)


def get_self_learn_stats():
    """ดู win rate จาก history เพื่อ calibrate confidence"""
    log = [t for t in load_trade_log() if t.get('outcome') is not None]
    if not log:
        return {}
    total  = len(log)
    wins   = sum(1 for t in log if t['outcome'] == 'WIN')
    by_sym = defaultdict(lambda: {'wins': 0, 'total': 0})
    for t in log:
        by_sym[t['symbol']]['total'] += 1
        if t['outcome'] == 'WIN':
            by_sym[t['symbol']]['wins'] += 1
    return {
        'overall_win_rate': wins / total,
        'by_symbol':        dict(by_sym),
        'total_trades':     total,
    }

