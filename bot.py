def mtf_bias(mtf_data):
    """
    วิเคราะห์ทิศทาง (bias) ของแต่ละ timeframe
    Return: dict {tf: ('BUY'|'SELL'|'NEUTRAL', strength 0-1)}
    """
    result = {}

    for tf_name, df in mtf_data.items():
        if df is None or len(df) < 50:
            result[tf_name] = ('NEUTRAL', 0.5)
            continue
        try:
            df = compute_indicators(df)
            last = df.iloc[-1]
            buy_pts = sell_pts = 0

            # 1. EMA trend
            if last['EMA9'] > last['EMA20'] > last['EMA50']:
                buy_pts += 2
            elif last['EMA9'] < last['EMA20'] < last['EMA50']:
                sell_pts += 2

            # 2. Price vs EMA200 (macro trend)
            if last['Close'] > last['EMA200']:
                buy_pts += 1
            else:
                sell_pts += 1

            # 3. ADX direction
            if last['ADX'] > MIN_ADX:
                if last['ADX_pos'] > last['ADX_neg']:
                    buy_pts += 1
                else:
                    sell_pts += 1

            # 4. MACD
            if last['MACD'] > last['MACD_signal'] and last['MACD_hist'] > 0:
                buy_pts += 1
            elif last['MACD'] < last['MACD_signal'] and last['MACD_hist'] < 0:
                sell_pts += 1

            # 5. Ichimoku cloud
            cloud_top = max(last['ICHI_span_a'], last['ICHI_span_b'])
            cloud_bot = min(last['ICHI_span_a'], last['ICHI_span_b'])
            if last['Close'] > cloud_top:
                buy_pts += 1
            elif last['Close'] < cloud_bot:
                sell_pts += 1

            # 6. Hurst (trending regime)
            if last['HURST'] > 0.55:
                if buy_pts > sell_pts:
                    buy_pts += 0.5
                else:
                    sell_pts += 0.5

            total = buy_pts + sell_pts
            if total == 0:
                result[tf_name] = ('NEUTRAL', 0.5)
                continue

            bias    = 'BUY' if buy_pts >= sell_pts else 'SELL'
            strength = max(buy_pts, sell_pts) / total
            result[tf_name] = (bias, strength, df)

        except Exception as e:
            result[tf_name] = ('NEUTRAL', 0.5)

    return result


def mtf_confluence_score(mtf_biases):
    """
    คำนวณ confluence: ทุก TF เห็นด้วยหรือไม่
    Return: (signal, confluence_score 0-1, alignment_count)
    """
    weighted_buy = weighted_sell = 0
    aligned = 0

    for tf_name, cfg in MTF_CONFIG.items():
        if tf_name not in mtf_biases:
            continue
        bias_info = mtf_biases[tf_name]
        bias      = bias_info[0]
        strength  = bias_info[1]
        w         = cfg['weight']

        if bias == 'BUY':
            weighted_buy  += w * strength
        elif bias == 'SELL':
            weighted_sell += w * strength

    signal = 'BUY' if weighted_buy >= weighted_sell else 'SELL'
    total  = weighted_buy + weighted_sell
    confluence = max(weighted_buy, weighted_sell) / total if total > 0 else 0.5

    # bonus: slow TF เห็นด้วยกับ medium
    slow_bias   = mtf_biases.get('slow',   ('NEUTRAL',))[0]
    medium_bias = mtf_biases.get('medium', ('NEUTRAL',))[0]
    fast_bias   = mtf_biases.get('fast',   ('NEUTRAL',))[0]

    alignment = sum([
        slow_bias   == signal,
        medium_bias == signal,
        fast_bias   == signal,
    ])

    # เพิ่ม confidence ถ้า alignment สูง
    confluence_bonus = (alignment - 1) * 0.05  # +5% per extra alignment
    confluence = min(1.0, confluence + confluence_bonus)

    return signal, confluence, alignment

