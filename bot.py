# ================== RISK MANAGEMENT ==================
def compute_risk(df, signal, conf):
    last  = df.iloc[-1]
    price = float(last['Close'])
    atr   = float(last['ATR'])

    # Dynamic multiplier: volatility regime ปรับ SL
    vol_r = float(last['VOL_REGIME'])  # 0=low, 1=normal, 2=high
    sl_mult = ATR_SL_MULT * (1 + vol_r * 0.1)
    tp_mult = ATR_TP_MULT * (1 + vol_r * 0.05)

    if signal == 'BUY':
        SL = price - sl_mult * atr
        TP = price + tp_mult * atr
    else:
        SL = price + sl_mult * atr
        TP = price - tp_mult * atr

    risk   = abs(price - SL)
    reward = abs(price - TP)
    rr     = reward / risk if risk > 0 else 0

    # Kelly Criterion
    kelly = max(0, min((rr * conf - (1 - conf)) / rr, 0.25)) * 100

    # ATR-based estimated hold time (1h bars)
    est_bars   = reward / atr
    est_hours  = est_bars  # เนื่องจาก medium TF = 1h

    return price, SL, TP, rr, kelly, atr, est_hours

