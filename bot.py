# ================== BACKTEST ==================
def backtest(df, n=100):
    d = df.copy().tail(n)
    d['sig'] = np.where(d['EMA9'] > d['EMA20'], 1, -1)
    d['ret'] = d['Close'].pct_change()
    d['sr']  = d['sig'].shift(1) * d['ret']
    d['cum'] = (1 + d['sr']).cumprod()
    tot_ret  = d['cum'].iloc[-1] - 1
    win_rate = (d['sr'] > 0).sum() / len(d)
    std      = d['sr'].std()
    sharpe   = d['sr'].mean() / std * np.sqrt(252 * 24) if std > 0 else 0
    dd       = (d['cum'] / d['cum'].cummax() - 1).min()
    profit_factor = d['sr'][d['sr']>0].sum() / abs(d['sr'][d['sr']<0].sum()) if (d['sr']<0).any() else 99
    return {
        'total_return':   tot_ret * 100,
        'win_rate':       win_rate * 100,
        'sharpe':         sharpe,
        'max_drawdown':   dd * 100,
        'profit_factor':  profit_factor,
        'total_trades':   int((d['sig'].diff() != 0).sum()),
    }

