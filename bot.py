def compute_indicators(df):
    c = df['Close']
    h = df['High']
    l = df['Low']
    v = df.get('Volume', pd.Series(np.ones(len(df)), index=df.index))
    if hasattr(v, 'squeeze'): v = v.squeeze()

    # ── TREND ─────────────────────────────────────────────────────────
    for w in [9, 20, 50, 100, 200]:
        df[f'EMA{w}'] = ta.trend.ema_indicator(c, window=w)
    df['SMA20']  = ta.trend.sma_indicator(c, window=20)
    df['SMA200'] = ta.trend.sma_indicator(c, window=200)

    adx = ta.trend.ADXIndicator(h, l, c, window=14)
    df['ADX']     = adx.adx()
    df['ADX_pos'] = adx.adx_pos()
    df['ADX_neg'] = adx.adx_neg()

    macd = ta.trend.MACD(c)
    df['MACD']        = macd.macd()
    df['MACD_signal'] = macd.macd_signal()
    df['MACD_hist']   = macd.macd_diff()

    # Ichimoku
    ichi = ta.trend.IchimokuIndicator(h, l)
    df['ICHI_conv']   = ichi.ichimoku_conversion_line()
    df['ICHI_base']   = ichi.ichimoku_base_line()
    df['ICHI_span_a'] = ichi.ichimoku_a()
    df['ICHI_span_b'] = ichi.ichimoku_b()

    # Parabolic SAR
    df['PSAR']    = ta.trend.PSARIndicator(h, l, c).psar()
    df['PSAR_bull']= ta.trend.PSARIndicator(h, l, c).psar_up_indicator()
    df['PSAR_bear']= ta.trend.PSARIndicator(h, l, c).psar_down_indicator()

    # ── MOMENTUM ──────────────────────────────────────────────────────
    df['RSI']      = ta.momentum.rsi(c, window=14)
    df['RSI7']     = ta.momentum.rsi(c, window=7)
    df['RSI21']    = ta.momentum.rsi(c, window=21)

    stoch = ta.momentum.StochasticOscillator(h, l, c)
    df['STOCH_k']  = stoch.stoch()
    df['STOCH_d']  = stoch.stoch_signal()

    df['ROC10']    = ta.momentum.roc(c, window=10)
    df['ROC20']    = ta.momentum.roc(c, window=20)
    df['WILLR']    = ta.momentum.williams_r(h, l, c)
    df['CCI']      = ta.trend.CCIIndicator(h, l, c).cci()
    df['TSI']      = ta.momentum.TSIIndicator(c).tsi()  # True Strength Index

    # ── VOLATILITY ────────────────────────────────────────────────────
    for w, dev in [(20,2), (20,1), (20,3)]:
        bb = ta.volatility.BollingerBands(c, window=w, window_dev=dev)
        suffix = '' if dev == 2 else f'_{dev}'
        df[f'BB_high{suffix}'] = bb.bollinger_hband()
        df[f'BB_low{suffix}']  = bb.bollinger_lband()
    df['BB_mid']   = ta.volatility.BollingerBands(c).bollinger_mavg()
    df['BB_width'] = (df['BB_high'] - df['BB_low']) / df['BB_mid']
    df['BB_pct']   = ta.volatility.BollingerBands(c).bollinger_pband()

    atr = ta.volatility.AverageTrueRange(h, l, c, window=14)
    df['ATR']      = atr.average_true_range()
    df['ATR_pct']  = df['ATR'] / c * 100

    kc = ta.volatility.KeltnerChannel(h, l, c)
    df['KC_high']  = kc.keltner_channel_hband()
    df['KC_low']   = kc.keltner_channel_lband()
    df['SQUEEZE']  = (df['BB_high'] < df['KC_high']) & (df['BB_low'] > df['KC_low'])

    # Donchian Channel (breakout)
    df['DC_high']  = h.rolling(20).max()
    df['DC_low']   = l.rolling(20).min()

    # ── VOLUME ────────────────────────────────────────────────────────
    v_safe = v.replace(0, np.nan).fillna(1)
    df['OBV']      = ta.volume.on_balance_volume(c, v)
    df['CMF']      = ta.volume.chaikin_money_flow(h, l, c, v)
    df['MFI']      = ta.volume.money_flow_index(h, l, c, v)
    df['VWAP']     = (((h + l + c) / 3) * v).cumsum() / v.cumsum()
    df['VOL_MA']   = v.rolling(20).mean()
    df['VOL_RATIO']= v / df['VOL_MA'].replace(0, np.nan)
    df['AD']       = ta.volume.acc_dist_index(h, l, c, v)  # Accumulation/Distribution

    # ── FIBONACCI ─────────────────────────────────────────────────────
    n = min(55, len(df))
    hi = df['High'].rolling(n).max()
    lo = df['Low'].rolling(n).min()
    rng = hi - lo
    for lvl, pct in [('236', 0.236), ('382', 0.382), ('500', 0.500),
                     ('618', 0.618), ('786', 0.786)]:
        df[f'FIB_{lvl}'] = hi - pct * rng

    # ── MARKET REGIME ─────────────────────────────────────────────────
    # Hurst Exponent (>0.5 = trending, <0.5 = mean-reverting)
    def hurst_exp(ts, max_lag=20):
        if len(ts) < max_lag * 2:
            return 0.5
        lags = range(2, max_lag)
        tau = []
        for lag in lags:
            diff = ts[lag:] - ts[:-lag]
            tau.append(np.sqrt(np.std(diff)))
        try:
            slope, *_ = linregress(np.log(list(lags)), np.log(tau))
            return slope * 2.0
        except:
            return 0.5

    df['HURST'] = c.rolling(40).apply(
        lambda x: hurst_exp(x.values), raw=False)

    # Linear Regression Slope (normalized)
    def lr_slope_norm(series, n=20):
        slopes = [np.nan] * n
        for i in range(n, len(series)):
            y = series.iloc[i-n:i].values
            x = np.arange(n)
            slope, *_ = linregress(x, y)
            slopes.append(slope / (y.mean() + 1e-10) * 100)  # normalize
        return pd.Series(slopes, index=series.index)

    df['LR_SLOPE'] = lr_slope_norm(c)

    # Volatility Regime (low/normal/high)
    atr_ma = df['ATR'].rolling(50).mean()
    df['VOL_REGIME'] = np.where(df['ATR'] > atr_ma * 1.3, 2,
                        np.where(df['ATR'] < atr_ma * 0.7, 0, 1))

    df.dropna(inplace=True)
    return df

