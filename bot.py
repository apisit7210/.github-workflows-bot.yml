# ================== ML ENGINE (ENSEMBLE) ==================
def build_features(df):
    cols = [
        'RSI', 'RSI7', 'RSI21', 'MACD_hist', 'STOCH_k', 'STOCH_d',
        'BB_pct', 'BB_width', 'ATR_pct', 'ADX', 'ADX_pos', 'ADX_neg',
        'ROC10', 'ROC20', 'WILLR', 'CCI', 'TSI', 'CMF', 'MFI',
        'VOL_RATIO', 'LR_SLOPE', 'HURST', 'VOL_REGIME'
    ]
    return [c for c in cols if c in df.columns]


def train_ensemble(df):
    """
    Voting Ensemble: RF + GBM + Weighted vote
    Label: close[+5] > close * 1.005 → BUY (1)
    """
    df = df.copy()
    df['future'] = df['Close'].shift(-5)
    df['label']  = ((df['future'] / df['Close'] - 1) > 0.005).astype(int)
    df.dropna(inplace=True)

    feat_cols = build_features(df)
    X = df[feat_cols].values
    y = df['label'].values

    if len(X) < 80:
        return None, None, 0.5, 0.5, feat_cols

    tscv   = TimeSeriesSplit(n_splits=4)
    scaler = StandardScaler()

    rf  = RandomForestClassifier(n_estimators=150, max_depth=6,
                                  min_samples_leaf=5, random_state=42, n_jobs=-1)
    gbm = GradientBoostingClassifier(n_estimators=100, max_depth=4,
                                      learning_rate=0.05, random_state=42)
    ensemble = VotingClassifier([('rf', rf), ('gbm', gbm)], voting='soft')

    accs, precs = [], []
    for tr, te in tscv.split(X):
        Xtr = scaler.fit_transform(X[tr])
        Xte = scaler.transform(X[te])
        ensemble.fit(Xtr, y[tr])
        pred = ensemble.predict(Xte)
        accs.append(accuracy_score(y[te], pred))
        if pred.sum() > 0:
            precs.append(precision_score(y[te], pred, zero_division=0))

    avg_acc  = np.mean(accs)
    avg_prec = np.mean(precs) if precs else 0.5

    # Final fit
    X_scaled = scaler.fit_transform(X)
    ensemble.fit(X_scaled, y)

    return ensemble, scaler, avg_acc, avg_prec, feat_cols


def ml_prob(model, scaler, df, feat_cols):
    if model is None:
        return 0.5
    x = scaler.transform(df[feat_cols].iloc[-1:].values)
    return model.predict_proba(x)[0][1]

