def rule_score(df):
    """
    Rule-based scoring: 20+ conditions, return (signal, ratio, buy, sell, reasons)
    """
    last  = df.iloc[-1]
    prev  = df.iloc[-2]
    prev2 = df.iloc[-3]
    buy_sc = sell_sc = 0
    rb = []   # reasons buy
    rs = []   # reasons sell

    def b(pts, reason): nonlocal buy_sc;  buy_sc  += pts; rb.append(reason)
    def s(pts, reason): nonlocal sell_sc; sell_sc += pts; rs.append(reason)

    # ── 1. EMA Structure ──────────────────────────────────────────────
    if last['EMA9'] > last['EMA20'] > last['EMA50'] > last['EMA100']:
        b(3, 'Full EMA bullish stack (9>20>50>100)')
    elif last['EMA9'] < last['EMA20'] < last['EMA50'] < last['EMA100']:
        s(3, 'Full EMA bearish stack (9<20<50<100)')
    elif last['EMA9'] > last['EMA20'] > last['EMA50']:
        b(2, 'EMA bullish stack (9>20>50)')
    elif last['EMA9'] < last['EMA20'] < last['EMA50']:
        s(2, 'EMA bearish stack (9<20<50)')

    # Golden/Death Cross (EMA20/EMA50)
    if prev['EMA20'] <= prev['EMA50'] and last['EMA20'] > last['EMA50']:
        b(2, 'Golden Cross EMA20/50')
    elif prev['EMA20'] >= prev['EMA50'] and last['EMA20'] < last['EMA50']:
        s(2, 'Death Cross EMA20/50')

    # ── 2. ADX Trend Strength ─────────────────────────────────────────
    adx = last['ADX']
    if adx > 30:
        if last['ADX_pos'] > last['ADX_neg']: b(2, f'ADX={adx:.0f} very strong uptrend')
        else:                                  s(2, f'ADX={adx:.0f} very strong downtrend')
    elif adx > MIN_ADX:
        if last['ADX_pos'] > last['ADX_neg']: b(1, f'ADX={adx:.0f} uptrend')
        else:                                  s(1, f'ADX={adx:.0f} downtrend')

    # ── 3. MACD ───────────────────────────────────────────────────────
    if prev['MACD'] < prev['MACD_signal'] and last['MACD'] > last['MACD_signal']:
        b(2, 'MACD bullish crossover')
    elif prev['MACD'] > prev['MACD_signal'] and last['MACD'] < last['MACD_signal']:
        s(2, 'MACD bearish crossover')
    if last['MACD_hist'] > 0 and last['MACD_hist'] > prev['MACD_hist'] > prev2['MACD_hist']:
        b(1, 'MACD histogram accelerating up')
    elif last['MACD_hist'] < 0 and last['MACD_hist'] < prev['MACD_hist'] < prev2['MACD_hist']:
        s(1, 'MACD histogram accelerating down')

    # ── 4. RSI (Divergence-aware) ──────────────────────────────────────
    rsi = last['RSI']
    if rsi < 25:    b(3, f'RSI deeply oversold ({rsi:.0f})')
    elif rsi < 35:  b(2, f'RSI oversold ({rsi:.0f})')
    elif rsi < 45:  b(1, f'RSI near oversold ({rsi:.0f})')
    elif rsi > 75:  s(3, f'RSI deeply overbought ({rsi:.0f})')
    elif rsi > 65:  s(2, f'RSI overbought ({rsi:.0f})')
    elif rsi > 55:  s(1, f'RSI near overbought ({rsi:.0f})')

    # RSI Momentum (fast > slow = bullish momentum)
    if last['RSI7'] > last['RSI21'] and prev['RSI7'] <= prev['RSI21']:
        b(1, 'RSI7 crossed above RSI21')
    elif last['RSI7'] < last['RSI21'] and prev['RSI7'] >= prev['RSI21']:
        s(1, 'RSI7 crossed below RSI21')

    # ── 5. Bollinger Bands ────────────────────────────────────────────
    bp = last['BB_pct']
    if last['Close'] < last['BB_low_1']:  b(2, 'Price below BB lower (1σ)')
    elif last['Close'] < last['BB_low']:  b(1, 'Price below BB lower (2σ)')
    if last['Close'] > last['BB_high_1']: s(2, 'Price above BB upper (1σ)')
    elif last['Close'] > last['BB_high']: s(1, 'Price above BB upper (2σ)')

    # BB Squeeze breakout
    if last['SQUEEZE'] and not prev['SQUEEZE']:
        if last['LR_SLOPE'] > 0: b(2, 'BB Squeeze release (bullish)')
        else:                     s(2, 'BB Squeeze release (bearish)')

    # Donchian breakout
    if last['Close'] >= last['DC_high']:  b(2, 'Donchian 20 high breakout')
    elif last['Close'] <= last['DC_low']: s(2, 'Donchian 20 low breakdown')

    # ── 6. Stochastic ─────────────────────────────────────────────────
    if last['STOCH_k'] < 20 and last['STOCH_k'] > last['STOCH_d']:
        b(2, f"Stoch bullish cross OS zone ({last['STOCH_k']:.0f})")
    elif last['STOCH_k'] < 20:
        b(1, f"Stoch oversold ({last['STOCH_k']:.0f})")
    if last['STOCH_k'] > 80 and last['STOCH_k'] < last['STOCH_d']:
        s(2, f"Stoch bearish cross OB zone ({last['STOCH_k']:.0f})")
    elif last['STOCH_k'] > 80:
        s(1, f"Stoch overbought ({last['STOCH_k']:.0f})")

    # ── 7. Ichimoku ───────────────────────────────────────────────────
    cloud_top = max(last['ICHI_span_a'], last['ICHI_span_b'])
    cloud_bot = min(last['ICHI_span_a'], last['ICHI_span_b'])
    if   last['Close'] > cloud_top: b(2, 'Above Ichimoku cloud')
    elif last['Close'] < cloud_bot: s(2, 'Below Ichimoku cloud')
    if prev['ICHI_conv'] <= prev['ICHI_base'] and last['ICHI_conv'] > last['ICHI_base']:
        b(2, 'Ichimoku TK bullish cross')
    elif prev['ICHI_conv'] >= prev['ICHI_base'] and last['ICHI_conv'] < last['ICHI_base']:
        s(2, 'Ichimoku TK bearish cross')

    # ── 8. Parabolic SAR ──────────────────────────────────────────────
    if last['PSAR_bull'] == 1:  b(1, 'PSAR bullish flip')
    elif last['PSAR_bear'] == 1: s(1, 'PSAR bearish flip')

    # ── 9. VWAP ───────────────────────────────────────────────────────
    if   last['Close'] > last['VWAP'] * 1.002: b(1, 'Price above VWAP')
    elif last['Close'] < last['VWAP'] * 0.998: s(1, 'Price below VWAP')

    # ── 10. Volume ────────────────────────────────────────────────────
    if last['VOL_RATIO'] > 1.8:
        if last['CMF'] > 0.1: b(2, f"Volume spike + CMF={last['CMF']:.2f}")
        elif last['CMF'] < -0.1: s(2, f"Volume spike + CMF={last['CMF']:.2f}")
    if last['MFI'] < 20: b(1, f"MFI oversold ({last['MFI']:.0f})")
    elif last['MFI'] > 80: s(1, f"MFI overbought ({last['MFI']:.0f})")

    # AD trend
    if last['AD'] > prev['AD'] > prev2['AD']:  b(1, 'A/D accumulation')
    elif last['AD'] < prev['AD'] < prev2['AD']: s(1, 'A/D distribution')

    # ── 11. TSI ───────────────────────────────────────────────────────
    if   last['TSI'] > 0 and prev['TSI'] <= 0: b(1, 'TSI crossed above 0')
    elif last['TSI'] < 0 and prev['TSI'] >= 0: s(1, 'TSI crossed below 0')

    # ── 12. CCI + Williams %R ─────────────────────────────────────────
    if last['CCI'] < -150:   b(1, f"CCI deep oversold ({last['CCI']:.0f})")
    elif last['CCI'] > 150:  s(1, f"CCI deep overbought ({last['CCI']:.0f})")
    if last['WILLR'] < -90:  b(1, f"Williams %R oversold ({last['WILLR']:.0f})")
    elif last['WILLR'] > -10: s(1, f"Williams %R overbought ({last['WILLR']:.0f})")

    # ── 13. Fibonacci ─────────────────────────────────────────────────
    for lvl in ['618', '500', '382']:
        key = f'FIB_{lvl}'
        if abs(last['Close'] - last[key]) / last['Close'] < 0.003:
            if buy_sc > sell_sc: b(1, f'Near Fib {lvl}')
            else:                 s(1, f'Near Fib {lvl}')
            break

    # ── 14. Market Regime filter ──────────────────────────────────────
    # ในช่วง sideways (Hurst < 0.45) ลด confidence
    regime_ok = last['HURST'] > 0.45

    total = buy_sc + sell_sc
    if total == 0:
        return 'NEUTRAL', 0.5, 0, 0, [], regime_ok

    signal = 'BUY' if buy_sc >= sell_sc else 'SELL'
    ratio  = max(buy_sc, sell_sc) / total
    reasons = rb if signal == 'BUY' else rs

    return signal, ratio, buy_sc, sell_sc, reasons, regime_ok

